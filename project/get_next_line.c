#include "get_next_line.h"

char	*get_next_line(int fd)
{
	char	*buf;
	size_t	buf_size;

	buf_size = 10;
	buf = (char *)ft_calloc(sizeof(char), buf_size);
	if (buf == NULL)
		return (NULL);
	buf = put_line(buf, buf_size, fd);
	return (buf);
}
